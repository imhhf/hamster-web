/**
 * code转url
 * @param {String} code 例如：cn 中国
 * @returns
 */
export const codeToUrl = (code) => {
  const { url } = countryIconUrl.find((item) => item.code.toLowerCase() === code.toLowerCase()) || {};
  if (url) {
    return require(`@/assets/img/country_icon/${url}.png`);
  }
  return '';
};

